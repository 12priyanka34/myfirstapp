package Test1;

public class LoopsAndConditionalStatements {

	
	public static void main(String[] args) {
		
		/*
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc"); System.out.println("abc");
		 * System.out.println("abc");
		 */
		
		// Loop First type: For loop		
		
		for(int x=1;x<=20;x=x+1) {
			System.out.println("abc");
		}
		
		
		//Loop Second Type: while Loop
		int x=1;
		while(x<=20) {
			System.out.println("abc");
			x=x+1;
		}
		
		//Loop 3rd Type: do while loop
		
		int p=1;
		do {
			System.out.println("abc");
			p=p+1;
		}while(p<=20);
		
		
		
		
		
	}
	
	
	
	
	
	
}
